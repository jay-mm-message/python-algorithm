# Using the build-in sort algorithm for python

cars = ['honda', 'bmw', 'toyota', 'ford']
print("The result is current: {}".format(cars))
cars.sort()
print("The result after using build-in sort algorithm for python: {}"
      .format(cars))
nums = [5, 3, 9, 2]
print("The result is current: {}".format(nums))
nums.sort()
print("The result after using build-in sort for python: {}".format(nums))
